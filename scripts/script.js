const sections = ["about-me", "academic-experience", "professional-experience", "projects", "publications"];

let currentLanguage = 'en';
let currentSection = 'about-me'

window.onload = function () {
    document.getElementById("section-toggle-about-me").className = "active-section";

    loadText(currentLanguage);
}

function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'pt' : 'en';
    loadText(currentLanguage);
}

function loadText(currentLanguage) {
    document.documentElement.lang = currentLanguage;

    document.getElementById("linkedin-link").href = translations[currentLanguage].linkedInLink;

    document.getElementById("language-toggle").innerText = translations[currentLanguage].languageToggle;

    document.getElementById("subtitle").innerHTML = translations[currentLanguage].subtitle;


    document.getElementById("about-me-title").innerHTML = translations[currentLanguage].aboutMeTitle;
    document.getElementById("about-me-content").innerHTML = translations[currentLanguage].aboutMeContent;
    document.getElementById("section-toggle-about-me").innerText = translations[currentLanguage].aboutMeTitle;


    document.getElementById("academic-experience-title").innerText = translations[currentLanguage].academicExperienceTitle;
    document.getElementById("academic-experience-content").innerHTML = translations[currentLanguage].academicExperienceContent;
    document.getElementById("research-interests-title").innerHTML = translations[currentLanguage].researchInterestsTitle;
    document.getElementById("research-interests-content").innerHTML = translations[currentLanguage].researchInterestsContent;
    document.getElementById("section-toggle-academic-experience").innerText = translations[currentLanguage].academicExperienceTitle;

    document.getElementById("professional-experience-title").innerText = translations[currentLanguage].professionalExperienceTitle;
    document.getElementById("professional-experience-content").innerHTML = translations[currentLanguage].professionalExperienceContent;
    document.getElementById("section-toggle-professional-experience").innerText = translations[currentLanguage].professionalExperienceTitle;

    document.getElementById("projects-title").innerText = translations[currentLanguage].projectsTitle;
    document.getElementById("projects-content").innerHTML = translations[currentLanguage].ProjectsContent;
    document.getElementById("section-toggle-projects").innerText = translations[currentLanguage].projectsTitle;

    document.getElementById("publications-title").innerText = translations[currentLanguage].publicationsTitle;
    document.getElementById("publications-content").innerHTML = translations[currentLanguage].publicationsContent;
    document.getElementById("section-toggle-publications").innerText = translations[currentLanguage].publicationsTitle;
}

function toggleSection(nextSection) {

    currentSection = nextSection;

    sections.forEach(element => {

        if (element === nextSection) {
            document.getElementById(element).style.display = "block";
            document.getElementById("section-toggle-" + element).className = "active-section";
        } else {
            document.getElementById(element).style.display = "none";
            document.getElementById("section-toggle-" + element).className = "";

        }
    });

}